import api from "./client";

export const registerUser = (data) =>
  api.post("/api/auth/register", data);

export const loginUser = (data) =>
  api.post("/api/auth/login", data);

export const getMe = (token) =>
  api.get("/api/auth/me", {
    headers: {
      Authorization: `Bearer ${token}`,
    },
  });
